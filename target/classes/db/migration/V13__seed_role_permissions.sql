SELECT
    gen_random_uuid(),
    r.id,
    p.id,
    now(),
    now(),
    NULL,
    NULL,
    FALSE
FROM expanded e
JOIN auth.roles r ON r.code = e.role_code AND r.is_deleted = FALSE
JOIN auth.permissions p ON p.module_code = e.module_code AND p.action_code = e.action_code AND p.is_deleted = FALSE
WHERE NOT EXISTS (
    SELECT 1
    FROM auth.role_permissions rp
    WHERE rp.role_id = r.id
      AND rp.permission_id = p.id
      AND rp.is_deleted = FALSE
);

